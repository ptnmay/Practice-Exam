#include <unistd.h>
#include <stdio.h>

int main(int ac, char **av)
{
	int i = 0;
	int j = 0;

	if (ac == 2)
	{
			while (av[1][i])
				i++;
			while(av[1][i - 1] == 32 || (av[1][i - 1] >= 9 && av[1][i - 1] <= 13))
			{
				i--;
			}
			while(i != 0 && ((av[1][i - 1] != 32) || (av[1][i] >= 9 && av[1][i] <= 13)))
			{
				i--;
				j++;
			}
			//i = i + 1;
			write(1, &av[1][i], j);
			// printf("i = %d\n", i);
			// printf("j = %d\n", j);
	}
	write(1, "\n", 1);
}
//j = len of last word
