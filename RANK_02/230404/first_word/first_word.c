#include <unistd.h>

int tapce(char c)
{
    if (c >= 9 && c <= 13 || c == 32)
        return(1);
    return(0);
}

int main(int ac, char **av)
{
    if (ac == 2)
    {
        int i = 0;

        while(tapce(av[1][i]) == 1)
            i++;
        while(av[1][i] > 33)
        {
            write(1, &av[1][i], 1);
            i++;
        }
    }
    write(1, "\n", 1);
}