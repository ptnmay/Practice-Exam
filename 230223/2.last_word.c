#include <unistd.h>
#include <stdio.h>

int main(int ac, char **av)
{
	int i = 0;
	int j = 0;
	int k = 0;

	if (ac == 2)
	{
		while(av[1][i] == 32 || (av[1][i] >= 9 && av[1][i] <= 13))
		{
			i++;
			k++;
		}
		if (k == 0)
		{
			i = 0;
			while (av[1][i])
				i++;
			// while(i > 0)
			// {
			// 	if ((av[1][i] >= 9 && av[1][i] <= 13) || av[1][i] == 32)
			// 		i--;
			// }
			// while(i > 0)
			// {
			// 	write(1, &av[1][i], 1);
			// 	i--;
			// }
			while(av[1][i - 1] == 32 || (av[1][i - 1] >= 9 && av[1][i - 1] <= 13))
			{
				i--;
			}
			while(av[1][i - 1] != 32 || (av[1][i - 1] >= 9 && av[1][i - 1] <= 13))
			{
				i--;
				j++;
			}
			//i = i + 1;
			write(1, &av[1][i], j);
		}
	}
	write(1, "\n", 1);
}
